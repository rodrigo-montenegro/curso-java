package main;

public class Main {

	public static void main(String[] args) {
		// Operadores Arimeticos
		
		int variableX = 50, variableY = 10;
		int resultado;
		
		resultado = variableX + variableY;
		System.out.println(resultado);
		
		resultado = variableX - variableY;
		System.out.println(resultado);
		
		resultado = variableX * variableY;
		System.out.println(resultado);
		
		resultado = variableX / variableY;
		System.out.println(resultado);
		
		//++ aumenta en uno
		//-- decrementa en uno
		
		variableX *= 3;
		System.out.println(variableX);
		
		variableX += 100;
		System.out.println(variableX);
		
	}

}
